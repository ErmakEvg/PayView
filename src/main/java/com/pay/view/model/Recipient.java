package com.pay.view.model;

public class Recipient {
    private String Vspl;
    private String Bank;
    private String Personal_Acct;
    private String Application_Date;
    private String Application_Date_End;
    private String Service_Day;
    private String Coffice;
    private String Delivery_Day;
    private String Delivery_Site;

    public String getVspl() {
        return Vspl;
    }

    public void setVspl(String vspl) {
        Vspl = vspl;
    }

    public String getBank() {
        return Bank;
    }

    public void setBank(String bank) {
        Bank = bank;
    }

    public String getPersonal_Acct() {
        return Personal_Acct;
    }

    public void setPersonal_Acct(String personal_Acct) {
        Personal_Acct = personal_Acct;
    }

    public String getApplication_Date() {
        return Application_Date;
    }

    public void setApplication_Date(String application_Date) {
        Application_Date = application_Date;
    }

    public String getApplication_Date_End() {
        return Application_Date_End;
    }

    public void setApplication_Date_End(String application_Date_End) {
        Application_Date_End = application_Date_End;
    }

    public String getService_Day() {
        return Service_Day;
    }

    public void setService_Day(String service_Day) {
        Service_Day = service_Day;
    }

    public String getCoffice() {
        return Coffice;
    }

    public void setCoffice(String coffice) {
        Coffice = coffice;
    }

    public String getDelivery_Day() {
        return Delivery_Day;
    }

    public void setDelivery_Day(String delivery_Day) {
        Delivery_Day = delivery_Day;
    }

    public String getDelivery_Site() {
        return Delivery_Site;
    }

    public void setDelivery_Site(String delivery_Site) {
        Delivery_Site = delivery_Site;
    }

}
